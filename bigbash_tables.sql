-- First, create the ENUM type for status
CREATE TYPE status_enum AS ENUM ('Y', 'N');
CREATE TYPE delete_enum AS ENUM ('Y', 'N');

-- Then, create the cms_users table
CREATE TABLE cms_users (
  id SERIAL PRIMARY KEY,
  name VARCHAR(200) NOT NULL,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  mobile VARCHAR(250) NOT NULL,
  office VARCHAR(250),
  fax VARCHAR(250),
  company_name VARCHAR(255) NOT NULL,
  role INT REFERENCES cms_role(id) NOT NULL DEFAULT 3,
  fburl VARCHAR(250),
  xurl VARCHAR(250),
  linkedinurl VARCHAR(250),
  yturl VARCHAR(250),
  instaurl VARCHAR(250),
  country VARCHAR(100),
  gstin VARCHAR(100),
  address VARCHAR(255),
  address2 VARCHAR(255),
  image TEXT,
  company_logo text,
  status status_enum DEFAULT 'Y',
  deleted delete_enum DEFAULT 'N',
  "createdAt" TIMESTAMPTZ DEFAULT NOW(),
  "updatedAt" TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE cms_users
ADD COLUMN approval CHAR(1) DEFAULT 'N' NOT NULL;

CREATE TABLE cms_role (
  id SERIAL PRIMARY KEY,
  name VARCHAR(50) NOT NULL UNIQUE,
  description TEXT,
  status status_enum DEFAULT 'Y',
  "createdAt" TIMESTAMPTZ DEFAULT NOW(),
  "updatedAt" TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE website_types (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    slug TEXT UNIQUE,
    description TEXT,
    status status_enum DEFAULT 'Y',
    "createdAt" TIMESTAMP DEFAULT NOW(),
    "updatedAt" TIMESTAMP DEFAULT NOW()
);

